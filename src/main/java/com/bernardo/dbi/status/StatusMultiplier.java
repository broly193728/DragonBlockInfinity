package com.bernardo.dbi.status;

public class StatusMultiplier {
    public float str;
    public float dex;
    public float con;
    public float will;
    public float mnd;
    public float spi;

    public StatusMultiplier(float str, float dex, float con, float will, float mnd, float spi) {
        this.str = str;
        this.dex = dex;
        this.con = con;
        this.will = will;
        this.mnd = mnd;
        this.spi = spi;
    }
}
